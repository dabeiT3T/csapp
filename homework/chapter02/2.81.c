#include <stdio.h>

int main()
{

    // ~((1 << k) - 1)
    printf("%.8x\n", ~((1 << 4) - 1));

    // ((1 << k + j) - 1) - ((1 << j) - 1)
    // (1 << k + j) - 1 - (1 << j) + 1
    // (1 << k + j) - (1 << j)
    // (1 << j)((1 << k) - 1)
    // ((1 << k) - 1) << j
    printf("%.8x\n", (1 << 12) - (1 << 4));

    return 0;
}

